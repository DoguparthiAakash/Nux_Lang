# Animated spinner
spinner() {
    local pid=$1
    local message="$2"
    local spin='в Ӣв ҷв №в ёв јв ҙв Ұв §в Үв Ҹ'
    local i=0
    
    hide_cursor
    while kill -0 $pid 2>/dev/null; do
        local char="${spin:$i:1}"
        printf "\r    ${CYAN}${char}${NC}  ${message}"
        i=$(( (i + 1) % 10 ))
        sleep 0.1
    done
    show_cursor
}

# Progress bar
progress_bar() {
    local current=$1
    local total=$2
    local width=50
    local percent=$((current * 100 / total))
    local filled=$((current * width / total))
    local empty=$((width - filled))
    
    printf "\r    ${CYAN}["
    printf "%${filled}s" | tr ' ' 'в–Ҳ'
    printf "%${empty}s" | tr ' ' 'в–‘'
    printf "]${NC} ${WHITE}%3d%%${NC}" $percent
}

# Step indicator
step() {
    local num=$1
    local total=$2
    local message="$3"
    echo ""
    echo -e "    ${MAGENTA}[${num}/${total}]${NC} ${BOLD}${message}${NC}"
}

# Status messages
status_ok() {
    echo -e "\r    ${GREEN}${CHECK}${NC}  $1"
}

status_fail() {
    echo -e "\r    ${RED}${CROSS}${NC}  $1"
}

status_warn() {
    echo -e "    ${YELLOW}!${NC}  $1"
}

status_info() {
    echo -e "    ${BLUE}${ARROW}${NC}  $1"
}

# Animated task
run_task() {
    local message="$1"
    shift
    local command="$@"
    
    printf "    ${CYAN}в Ӣ${NC}  ${message}"
    
    # Run command in background and capture output
    local output
    output=$($command 2>&1) &
    local pid=$!
    
    spinner $pid "$message"
    wait $pid
    local status=$?
    
    if [ $status -eq 0 ]; then
        status_ok "$message"
    else
        status_fail "$message"
        echo -e "    ${DIM}${output}${NC}"
        return 1
    fi
}

